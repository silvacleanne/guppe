"""
2. Faça um programa que peça para o usuário digitar três valores inteiros e imprima a soma deles.
"""
numero1: int = int(input("Informe um primeiro inteiro: "))
numero2: int = int(input("Informe um segundo inteiro: "))
numero3: int = int(input("Informe um terceiro inteiro: "))

soma: int = int(numero1 + numero2 + numero3)

print(f"A soma dos numeros inteiros {numero1} com {numero2} e {numero3} eh {soma}")
